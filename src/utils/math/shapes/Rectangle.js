class Rectangle
{
	constructor(x, y, width, height)
	{
		this.x = x;
		this.y = y;
		this.width = width;
		this.height = height;
	}

	getX()
	{
		return this.x;
	}
	
	setX(x)
	{
		this.x = x;
	}
	
	getY()
	{
		return this.y;
	}
	
	setY(y)
	{
		this.y = y;
	}
	
	getWidth()
	{
		return this.width;
	}
	
	getHeight()
	{
		return this.height;
	}
}
